#include <iostream>
using namespace std;

int main() {
    // Declare and initialize variables of all basic data types
    int intVar = 10;                  // Integer
    float floatVar = 3.14f;           // Floating-point
    double doubleVar = 2.718281828;   // Double-precision floating-point
    char charVar = 'A';               // Character
    bool boolVar = true;              // Boolean
    short shortVar = 100;             // Short integer
    long longVar = 100000;            // Long integer
    long long longLongVar = 1000000;  // Long long integer
    unsigned int unsignedVar = 50;    // Unsigned integer

    // Print the values
    cout << "int: " << intVar << endl;
    cout << "float: " << floatVar << endl;
    cout << "double: " << doubleVar << endl;
    cout << "char: " << charVar << endl;
    cout << "bool: " << boolVar << endl;
    cout << "short: " << shortVar << endl;
    cout << "long: " << longVar << endl;
    cout << "long long: " << longLongVar << endl;
    cout << "unsigned int: " << unsignedVar << endl;

    return 0;
}
